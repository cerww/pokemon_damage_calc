export function calculateStatNoBoost(pkm: pokemon, stat: "hp" | "att" | "def" | "sp_att" | "sp_def" | "speed") {
    if (stat === "hp") {
        if (pkm.name === "Shedinja") {
            return 1;
        }
        return ((pkm.base_hp * 2 + pkm.hp_ivs + Math.floor(pkm.hp_evs / 4)) * pkm.level) / 100 + 10 + pkm.level;
    } else {
        let [base, boost, iv, ev] = stat === "att" ? [pkm.base_att, pkm.att_boost, pkm.att_ivs, pkm.att_evs] :
            stat === "def" ? [pkm.base_def, pkm.def_boost, pkm.def_ivs, pkm.def_evs] :
                stat === "sp_att" ? [pkm.base_sp_att, pkm.sp_att_boost, pkm.sp_att_ivs, pkm.sp_att_evs] :
                    stat === "sp_def" ? [pkm.base_sp_def, pkm.sp_def_boost, pkm.sp_def_ivs, pkm.sp_def_evs] :
                        [pkm.base_speed, pkm.speed_boost, pkm.speed_ivs, pkm.speed_evs];

        return Math.floor(((base * 2 + iv + Math.floor(ev / 4)) * pkm.level) / 100 + 5);

    }
}

export function stat_after_boost(stat: number, boost: number) {
    if (boost === 0) {
        return stat;
    }
    if (boost > 0) {
        return stat * (boost + 2) / 2;
    } else {
        return stat * (2 / (-boost + 2));
    }


}

export function poke_round(n: number) {
    const floating_part = n % 1;
    const decimal_part = n - floating_part;
    if (floating_part <= 0.5) {
        return decimal_part;//round down
    }
    return decimal_part + 1;
}

export function calculateActualStat(pkm: pokemon, stat: "hp" | "att" | "def" | "sp_att" | "sp_def" | "speed") {
    if (stat === "hp") {
        if (pkm.base_hp === 1) {
            return 1;
        }
        return Math.floor((((pkm.base_hp * 2 + pkm.hp_ivs) + Math.floor(pkm.hp_evs / 4)) * pkm.level))
            / 100 + 5 + pkm.level;
    } else {
        let [base, boost, iv, ev] =
            stat === "att" ? [pkm.base_att, pkm.att_boost, pkm.att_ivs, pkm.att_evs] :
                stat === "def" ? [pkm.base_def, pkm.def_boost, pkm.def_ivs, pkm.def_evs] :
                    stat === "sp_att" ? [pkm.base_sp_att, pkm.sp_att_boost, pkm.sp_att_ivs, pkm.sp_att_evs] :
                        stat === "sp_def" ? [pkm.base_sp_def, pkm.sp_def_boost, pkm.sp_def_ivs, pkm.sp_def_evs] :
                            [pkm.base_speed, pkm.speed_boost, pkm.speed_ivs, pkm.speed_evs];

        return Math.floor(
            stat_after_boost(
                Math.floor(((base * 2 + iv + Math.floor(ev / 4)) * pkm.level) / 100) + 5,
                boost)
        );

    }

}





